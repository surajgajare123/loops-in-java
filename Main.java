class Main{
    public static void main(String[] args){
        // Introduced java loops
        // while loop 
        int a = 0;
        while(a<=12){
            System.out.println("hello coder!");
            a++;
        }
        // do while loop
        int b = 0;
        do{
            System.out.println("hello do while loop!");
            b++;
        }while(b<5);
        // for loop
        for(int i = 0; i < 6; i++){
            System.out.println("hello here is for loop!");
        }
    }
}
